#include "mex.h"
#include "matfunctions.h"
/* */
/*-----------------------------------------------------------------------*/
/*-----------------------------------------------------------------------*/
/* The C function */
/*-----------------------------------------------------------------------*/
/*-----------------------------------------------------------------------*/
void perfomer(mwSize dim1,        
        mwSize dim2,
        const double *A,
        const double *B,
        double *C)
{
    /*-------------------------------------------------------------------*/
    /*Remaining B matrices*/
    /*-------------------------------------------------------------------*/
    matmul((mwSize)1,dim1,dim2,A,B,C);
}
/*-----------------------------------------------------------------------*/
/*-----------------------------------------------------------------------*/
/* The gateway function */
/*-----------------------------------------------------------------------*/
/*-----------------------------------------------------------------------*/
void mexFunction( int nlhs, mxArray *plhs[],
                  int nrhs, const mxArray *prhs[])
{
    /*-------------------------------------------------------------------*/
    /*Inputs and declaration*/
    /*-------------------------------------------------------------------*/
    size_t dim1, dim2;
    double *A;               /* input matrix */
    double *B;               /* input matrix */
    /*-------------------------------------------------------------------*/
    /* outputs */
    /*-------------------------------------------------------------------*/
    double *C;                /* output matrix */
    /*-------------------------------------------------------------------*/
    /* create a pointer to the real data in the input matrix  */
    /*-------------------------------------------------------------------*/
    dim1             =  mxGetM(prhs[1]);
    dim2             =  mxGetN(prhs[1]);
    A                =  mxGetPr(prhs[0]);
    B                =  mxGetPr(prhs[1]);
    /*-------------------------------------------------------------------*/
    /* get a pointer to the real data in the output matrix */
    /*-------------------------------------------------------------------*/
    plhs[0]          =  mxCreateDoubleMatrix((mwSize)1, (mwSize)dim2, mxREAL);    
    C                =  mxGetPr(plhs[0]);
    /*-------------------------------------------------------------------*/
    /* call the computational routine */
    /*-------------------------------------------------------------------*/
    perfomer((mwSize)dim1,(mwSize)dim2,A,B,C);
}


