%
%--------------------------------------------------------------------------
%--------------------------------------------------------------------------

function gradp               =  GradpAdjoint(dim,p_elem,DN_X) 

n_gauss                      =  size(DN_X,3);
gradp                        =  zeros(dim,dim,n_gauss);
for igauss=1:n_gauss
    %----------------------------------------------------------------------
    % Compute derivative of displacements (Dx0DX)
    %----------------------------------------------------------------------
    gradp(:,:,igauss)        =  p_elem*DN_X(:,:,igauss)';
end




