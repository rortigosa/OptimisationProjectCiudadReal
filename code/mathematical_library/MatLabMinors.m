%--------------------------------------------------------------------------
%--------------------------------------------------------------------------
%
% This function computes the minors of a dimxdim Matrix
% 
%--------------------------------------------------------------------------
%--------------------------------------------------------------------------
function minors  =  MatLabMinors(dim,Matrix)

minors  =  zeros(dim,1);

for idim=1:dim
    minors(idim)  =  det(Matrix(1:idim,1:idim));
end